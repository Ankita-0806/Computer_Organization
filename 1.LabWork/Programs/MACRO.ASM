TITLE "TO USE MACRO"
INIT MACRO
 MOV AX,DATASEG
 MOV DS,AX
ENDM

PRINT MACRO MSG
 LEA DX,MSG
 MOV AH,09H
 INT 21H 
ENDM

FINISH MACRO
 MOV AH,04CH
 INT 21H
ENDM

DATASEG SEGMENT 
  MSG DB,0AH,0DH,'ENTER A CHARACTER:$'
  STRG1 DB,0AH,0DH,'THE ENTERED CHARACTER IS:$'
  TEMP DB ?
DATASEG ENDS

CODESEG SEGMENT 
  ASSUME CS:CODESEG,DS:DATASEG

START:
  INIT
  PRINT MSG

  MOV AH,01H
  INT 21H
  MOV TEMP,AL

  PRINT STRG1

  MOV DL,TEMP
  MOV AH,02H
  INT 21H

  FINISH

CODESEG ENDS
END START
END