TITLE "TO COPY ONE STRING TO ANOTHER STRING"

DATASEG SEGMENT PARA 'DATA'
STR1 DB 'INTERSTELLAR','$'
STR2 DB 12 DUP (' '),'$'
STR3 DB 0AH,0DH,'AFTER COPYING ,THE SECOND STRING IS:',0AH,0DH,'$'
DATASEG ENDS

CODESEG SEGMENT PARA 'CODE'
          ASSUME CS:CODESEG,DS:DATASEG

BEGIN:
   MOV AX,DATASEG
   MOV DS,AX
   MOV ES,AX

CLD
MOV CX,12
LEA DI,STR2
LEA SI,STR1
REP MOVSB

MOV AH,09H
LEA DX,STR3
INT 21H

MOV AH,09H
LEA DX,STR2
INT 21H

MOV AH,4CH
INT 21H

CODESEG ENDS
END BEGIN
END
