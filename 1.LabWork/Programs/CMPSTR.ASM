; Program to compare two strings using string instructions
TITLE "TO COMPARE TWO STRINGS"

DATASEG SEGMENT PARA 'DATA'
   PARA_LIST1 LABEL BYTE
   MAX_LEN1 DB 20
   AC_LEN1 DB ?
   STRING1 DB 20 DUP (' ')

   PARA_LIST2 LABEL BYTE
   MAX_LEN2 DB 20
   AC_LEN2 DB ?
   STRING2 DB 20 DUP (' ')
   
   I DB ?
   J DB ?
   MSG1 DB 0AH,0DH,'ENTER THE FIRST STRING:',0AH,0DH,'$'
   MSG2 DB 0AH,0DH,'ENTER THE SECOND STRING:',0AH,0DH,'$'
   MSG3 DB 0AH,0DH,'STRINGS ARE EQUAL','$'
   MSG4 DB 0AH,0DH,'STRINGS ARE NOT EQUAL','$'
DATASEG ENDS

CODESEG SEGMENT PARA 'CODE'
   ASSUME CS:CODESEG,DS:DATASEG

   BEGIN:
   MOV AX,DATASEG
   MOV DS,AX
   MOV ES,AX
   
   LEA DX,MSG1
   MOV AH,09H
   INT 21H

   LEA DX,PARA_LIST1
   MOV AH,0AH
   INT 21H

   LEA DX,MSG2
   MOV AH,09H
   INT 21H

   LEA DX,PARA_LIST2
   MOV AH,0AH
   INT 21H

   MOV CL,AC_LEN1
   MOV DL,AC_LEN2

   CMP CL,DL

   JNE A30
         
   MOV I,CL
   LEA SI,STRING1

   MOV J,DL
   LEA DI,STRING2

   LEA SI,STRING1
   LEA DI,STRING2

   A10:
      MOV AL,[SI]
      MOV BL,[DI]
      CMP AL,BL
      JNE A30
      INC DI
      INC SI
      DEC CL
      CMP CL,00H
      JNZ A10

   A20:
      LEA DX,MSG3
      MOV AH,09H
      INT 21H
      JMP A40

   A30:
      LEA DX,MSG4
      MOV AH,09H
      INT 21H

   A40: 
      MOV AH,4CH
      INT 21H

CODESEG ENDS

END BEGIN
END


