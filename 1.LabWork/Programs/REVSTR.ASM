TITLE "TO DISPLAY THE REVERSE OF GIVEN STRING"

DATASEG SEGMENT PARA 'DATA'
  STR1 DB 'SWEEKRITI','$'
  STR2 DB 12 DUP (20H),'$'
  STR3 DB 0AH,0DH,'THE REVERSE OF THE STRING IS:',0AH,0DH,'$'
  STR4 DB 0AH,0DH,'THE ORIGINAL STRING IS:',0AH,0DH,'$'
DATASEG ENDS

CODESEG SEGMENT PARA 'CODE'
    ASSUME CS:CODESEG,DS:DATASEG

BEGIN:
  MOV AX,DATASEG
  MOV DS,AX
  MOV ES,AX
 
CLD 
MOV CX,9
LEA DI,STR2+8
LEA SI,STR1

L20:
   MOV AL,[SI]
   INC SI
   MOV [DI],AL
   DEC DI

   LOOP L20
   MOV AH,09H
   LEA DX,STR4
   INT 21H

   MOV AH,09H
   LEA DX,STR1
   INT 21H

   MOV AH,09H
   LEA DX,STR3
   INT 21H

   MOV AH,09H
   LEA DX,STR2
   INT 21H

MOV AH,4CH
INT 21H

CODESEG ENDS
END BEGIN
END